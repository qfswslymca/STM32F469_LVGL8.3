static int8_t grayscale[28*28] = {0};


void show_array(int8_t *matrix)
{
    for (int i = 0; i < 28; i++)
    {
        for (int j = 0; j < 28; j++)
        {
            printf("%3d ", matrix[i*28+j]);
        }
        printf("\n");
    }
}

void paint_array(int8_t *matrix, uint8_t x, uint8_t y)
{
    for (int i = -2; i < 2; i++)
    {
        for (int j = -2; j < 2; j++)
        {
            if ((i*i + j*j) < 4)
            {
                int r_x = x + i;
                int r_y = y + j;
                r_x = MIN(MAX(r_x, 0), 28);
                r_y = MIN(MAX(r_y, 0), 28);
                matrix[r_y*28+r_x] = 127;
            }
        }
    }
    //[y*28+x] = 255;

}

void clear_array(int8_t *matrix)
{
    for (int i = 0; i < 28; i++)
    {
        for (int j = 0; j < 28; j++)
        {
            matrix[i*28+j] = -128;
        }
    }
}

